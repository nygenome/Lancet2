auto PhredToErrorProb(u32 phred_score) -> f64 {
  auto const idx = phred_score > MAX_PHRED_SCORE ? static_cast<usize>(MAX_PHRED_SCORE)
                                                 : static_cast<usize>(phred_score);
  // idx is clamped to [0, MAX_PHRED_SCORE] above; the index is guaranteed in-bounds for the LUT.
  // NOLINTNEXTLINE(cppcoreguidelines-pro-bounds-constant-array-index)
  return LUT_PHRED_TO_ERROR_PROB[idx];
}

auto ErrorProbToPhred(f64 prob) -> f64 {
  if (prob == 1.0) {
    return 0;
  }
  if (prob == 0.0) {
    return MAX_PHRED_SCORE;
  }
  static constexpr f64 PHRED_MULTIPLIER = -10.0;
  return PHRED_MULTIPLIER * std::log10(prob);
}

